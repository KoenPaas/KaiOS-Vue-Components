<template>
  <div class="softkey">
    <label class="left">{{ key.left.string }}</label>
    <label class="center">{{ key.center.string }}</label>
    <label class="right">{{ key.right.string }}</label>
    <!-- v-on:SoftRight="back()" -->
  </div>
</template>

<script>
import store from '@/js/store'

export default {
  name: 'Softkey',
  computed: {
    key () {
      return store.keys
    }
  },
  methods: {
    onKeyDown (event) {
      switch (event.key) {
        // case 'ArrowLeft':
        case 'SoftLeft':
          this.key.left.fn()
          break
        case 'Enter':
          this.key.center.fn()
          break
        // case 'ArrowRight':
        case 'SoftRight':
          this.key.right.fn()
          break
      }
    }
  },
  mounted () {
    document.addEventListener('keydown', this.onKeyDown)
  },
  beforeDestroy () {
    document.removeEventListener('keydown', this.onKeyDown)
  }
}
</script>

